  FOR EACH ROW EXECUTE FUNCTION public.update_consultation_updated_at();

-- Function to notify doctors of new consultation
CREATE OR REPLACE FUNCTION public.notify_doctors_new_consultation()
RETURNS trigger
LANGUAGE plpgsql
SECURITY DEFINER
SET search_path = public
AS $$
BEGIN
  INSERT INTO public.notifications (user_id, title, message, type, consultation_id)
  SELECT 
    p.id,
    'New Consultation Request',
    'A new consultation request has been submitted: ' || NEW.title,
    'info',
    NEW.id
  FROM public.profiles p
  JOIN public.user_roles ur ON p.id = ur.user_id
  WHERE ur.role = 'doctor';
  
  RETURN NEW;
END;
$$;

CREATE TRIGGER on_consultation_created
  AFTER INSERT ON public.consultations
  FOR EACH ROW EXECUTE FUNCTION public.notify_doctors_new_consultation();

-- Function to accept consultation
CREATE OR REPLACE FUNCTION public.accept_consultation(consult_id uuid, doc_id uuid)
RETURNS boolean
LANGUAGE plpgsql
SET search_path = public
AS $$
DECLARE
  rows_updated INT;
BEGIN
  UPDATE public.consultations
  SET doctor_id = doc_id, status = 'in_progress'
  WHERE id = consult_id AND status = 'pending';

  GET DIAGNOSTICS rows_updated = ROW_COUNT;
  RETURN rows_updated = 1;
END;
$$;

-- Function to update doctor
CREATE OR REPLACE FUNCTION public.update_doctor(
  p_doctor_id uuid, 
  p_first_name text, 
  p_last_name text, 
  p_email text, 
  p_phone text, 
  p_specialty text, 
  p_profilepic text
)
RETURNS void
LANGUAGE plpgsql
SET search_path = public
AS $$
BEGIN
  UPDATE public.profiles
  SET first_name = p_first_name, last_name = p_last_name, email = p_email, phone = p_phone
  WHERE id = p_doctor_id;

  UPDATE public.doctors_info
  SET specialty = p_specialty, profile_pic = p_profilepic
  WHERE profile_id = p_doctor_id;
END;
$$;

-- Function for doctor registration notification
CREATE OR REPLACE FUNCTION public.notify_admins_new_doctor_registration()
RETURNS trigger
LANGUAGE plpgsql
SECURITY DEFINER
SET search_path = public
AS $$
BEGIN
  INSERT INTO public.notifications (user_id, title, message, type)
