-- 1. Profiles table (depends on auth.users)
CREATE TABLE public.profiles (
    id uuid NOT NULL REFERENCES auth.users(id) ON DELETE CASCADE,
    email text NOT NULL,
    first_name text,
    last_name text,
    phone text,
    dob date,
    gender public.user_gender,
    consent_privacy boolean DEFAULT false,
    consent_terms boolean DEFAULT false,
    consent_timestamp timestamp with time zone,
    created_at timestamp with time zone DEFAULT now() NOT NULL,
    updated_at timestamp with time zone DEFAULT now() NOT NULL,
    PRIMARY KEY (id)
);

-- 2. User roles table
CREATE TABLE public.user_roles (
    id uuid DEFAULT extensions.gen_random_uuid() NOT NULL,
    user_id uuid NOT NULL REFERENCES auth.users(id) ON DELETE CASCADE,
    role public.user_role NOT NULL,
    created_at timestamp with time zone DEFAULT now() NOT NULL,
    PRIMARY KEY (id),
    UNIQUE (user_id, role)
);

-- 3. Doctors info table
CREATE TABLE public.doctors_info (
    id bigint GENERATED BY DEFAULT AS IDENTITY NOT NULL,
    profile_id uuid NOT NULL REFERENCES auth.users(id) ON DELETE CASCADE,
    specialty text,
    license_number text,
    education text,
    bio text,
    certifications text,
    years_of_experience integer,
    profile_pic text,
    status public.status_enum DEFAULT 'active',
    created_at timestamp with time zone DEFAULT now(),
    PRIMARY KEY (id)
);

-- 4. Consultations table
CREATE TABLE public.consultations (
    id uuid DEFAULT extensions.gen_random_uuid() NOT NULL,
    patient_id uuid NOT NULL REFERENCES auth.users(id) ON DELETE CASCADE,
    doctor_id uuid REFERENCES auth.users(id) ON DELETE SET NULL,
    title text NOT NULL,
    description text,
    diagnosis text,
    observations text,
    treatment_plan text,
    images text[],
    status text DEFAULT 'pending',
    priority text DEFAULT 'normal',
    payment_id text,
    created_at timestamp with time zone DEFAULT now(),
    updated_at timestamp with time zone DEFAULT now(),
    PRIMARY KEY (id)
);

-- 5. Chats table
CREATE TABLE public.chats (
    id uuid DEFAULT extensions.gen_random_uuid() NOT NULL,
    consultation_id uuid NOT NULL REFERENCES public.consultations(id) ON DELETE CASCADE,
    doctor_id uuid REFERENCES auth.users(id) ON DELETE SET NULL,
    patient_id uuid REFERENCES auth.users(id) ON DELETE SET NULL,
    status character varying(20) DEFAULT 'active' NOT NULL,
    created_at timestamp with time zone DEFAULT now() NOT NULL,
    updated_at timestamp with time zone DEFAULT now() NOT NULL,
    PRIMARY KEY (id)
);

-- 6. Messages table
CREATE TABLE public.messages (
    id uuid DEFAULT extensions.gen_random_uuid() NOT NULL,
    chat_id uuid REFERENCES public.chats(id) ON DELETE SET NULL,
    consultation_id uuid NOT NULL REFERENCES public.consultations(id) ON DELETE CASCADE,
    sender_id uuid NOT NULL REFERENCES auth.users(id) ON DELETE CASCADE,
    content text NOT NULL,
    message_type text DEFAULT 'text',
    file_url text,
    created_at timestamp with time zone DEFAULT now(),
    PRIMARY KEY (id)
);

-- 7. Notifications table
CREATE TABLE public.notifications (
    id uuid DEFAULT extensions.gen_random_uuid() NOT NULL,
    user_id uuid NOT NULL REFERENCES auth.users(id) ON DELETE CASCADE,
    title text NOT NULL,
    message text NOT NULL,
    type text DEFAULT 'info',
    is_read boolean DEFAULT false,
    consultation_id uuid REFERENCES public.consultations(id) ON DELETE CASCADE,
    created_at timestamp with time zone DEFAULT now(),
    PRIMARY KEY (id)
);

-- 8. Media assets table
CREATE TABLE public.media_assets (
    id uuid DEFAULT extensions.gen_random_uuid() NOT NULL,
    consultation_id uuid REFERENCES public.consultations(id) ON DELETE SET NULL,
    image_url text,
    image_type text,
    created_at timestamp with time zone DEFAULT now(),
    PRIMARY KEY (id)
);

-- 9. Patient feedback table
CREATE TABLE public.patient_feedback (
    id uuid DEFAULT extensions.gen_random_uuid() NOT NULL,
    user_id uuid REFERENCES auth.users(id) ON DELETE SET NULL,
    consultation_id uuid REFERENCES public.consultations(id) ON DELETE SET NULL,
